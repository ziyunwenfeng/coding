package com.hibernatedemo;

public class Emp {
	private int id;
	private String name;
	private Addr addr;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public Addr getAddr() {
		return addr;
	}
	public void setAddr(Addr addr) {
		this.addr = addr;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
}
